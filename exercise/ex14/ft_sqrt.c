int		ft_sqrt(int nb)
{
	int		i;
	int		a;
	int		n;

	i = 0;
	n = 1;
	a = 0;
	while (a != nb)
	{
		if (i > 46340 || nb >= 2147483647)
			return (0);
		a = a + n;
		n = n + 2;
		i++;
	}
	return (i);
}
